<#
    ⏹
    Copy-Item
        复制文件或者目录
    Move-Item
        移动文件或者目录
#>

<#
    ⏹
    Get-Item
        获取指定的文件或者目录
    Get-Childitem
        列出目录的内容
    Get-ItemProperty
        获取文件或目录的属性
    Get-Content
        基于文本行来读取内容
#>

<#
    ⏹
    New-Item
        创建新文件或者目录
    Remove-Item
        删除空目录或者文件
    Rename-Item
        重命名文件或者路径
#>

<#
    ⏹
    Join-Path
        连接两个路径为一个路径
    Resolve-Path
        处理相对路径或者包含通配符的路径,相对路径转绝对路径
    Split-Path
        提取路径的特定部分,例如父目录,驱动器,文件名
    Test-Path
        测试指定的路径是否存在
#>

<#
    ⏹
    Set-ItemProperty
        设置文件或路径的属性
    Set-Location
        更改当前目录的位置,相当于 cd 命令
    Get-Location
        获取当前目录的位置,相当于 linux的 pwd 命令
#>