<#
⏹通配符
    .
        匹配除了换行符以外的任意字符
    [^abc]
        匹配除了中括号中的任意字符
    [^a-z]
        匹配除了中括号指定区间之外的任意字符
    [abc]
        匹配中括号中指定的任意一个字符
    [a-z]
        匹配中括号指定区间之内的任意字符
    \d
        匹配数字字符,等同于[0-9]
    \D
        匹配数字以外的字符
    \n
        换行符
    \r
        回车符
    \s
        空格(空格,制表符,新行)
    \S
        匹配空格(空格,制表符,新行)以外的字符
    \t
        制表符
    \w
        匹配字符,数字,下划线
    \W
        匹配字符,数字,下划线以外的字符
#>
<#
⏹限定符
    *
        匹配一个元素0次或者多次(最大限度地匹配)
    *?
        匹配前面的元素零次或者多次(最小限度地匹配)
    .*
        匹配任意数量的字符(包括0个字符)
    ?
        匹配上一个元素0次或者1次
    {n,}
        匹配上一个元素至少n次
    {n,m}
        匹配上一个元素n至m次
    {n}
        匹配上一个元素n次
    +
        匹配上一个元素一次或者多次
    $
        在字符串的结尾匹配
    ^
        在字符串的开始匹配
    \b
        在单词的边界匹配
    \B
        不在单词的边界匹配
#>